import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, "..", "..")

const pkgDir = path.join(repoRoot, "DeepFilterNet", "libDF", "pkg")
const outDir = path.join(repoRoot, "js", "src", "dist")

const bindgenSrcPath = path.join(pkgDir, "df.js")
const wasmSrcPath = path.join(pkgDir, "df_bg.wasm")

const bindgenOutPath = path.join(outDir, "deepfilter-bindgen.js")
const wasmBase64OutPath = path.join(outDir, "deepfilter-wasm-base64.js")
const syncOutPath = path.join(outDir, "deepfilter-sync.js")

if (!fs.existsSync(bindgenSrcPath) || !fs.existsSync(wasmSrcPath)) {
    throw new Error(
        "DeepFilterNet wasm package not found. Run `sh DeepFilterNet/scripts/build_wasm_package.sh` first.",
    )
}

fs.mkdirSync(outDir, { recursive: true })

const bindgenSource = fs.readFileSync(bindgenSrcPath, "utf8")
const bindgenOutput = `${bindgenSource.trimEnd()}\n\nexport default wasm_bindgen\n`
fs.writeFileSync(bindgenOutPath, bindgenOutput, "utf8")

const wasmBase64 = fs.readFileSync(wasmSrcPath).toString("base64")
const wasmBase64Module = `// Auto-generated by js/scripts/build-deepfilter-artifacts.mjs
/** @type {string} */
export const DEEPFILTER_WASM_BASE64 = ${JSON.stringify(wasmBase64)}
`
fs.writeFileSync(wasmBase64OutPath, wasmBase64Module, "utf8")

const syncModule = `// Auto-generated by js/scripts/build-deepfilter-artifacts.mjs
import wasmBindgen from "./deepfilter-bindgen.js"
import { DEEPFILTER_WASM_BASE64 } from "./deepfilter-wasm-base64.js"

let initialized = false

const BASE64_CHARS = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/"
const BASE64_LOOKUP = new Int16Array(256).fill(-1)

for (let index = 0; index < BASE64_CHARS.length; index += 1) {
    BASE64_LOOKUP[BASE64_CHARS.charCodeAt(index)] = index
}

function base64ToBytesWithAtob(base64) {
    const raw = globalThis.atob(base64)
    const bytes = new Uint8Array(raw.length)
    for (let index = 0; index < raw.length; index += 1) {
        bytes[index] = raw.charCodeAt(index)
    }
    return bytes
}

function base64ToBytesWithoutAtob(base64) {
    const sanitized = base64.replace(/[\\r\\n\\s]/g, "")
    if (sanitized.length % 4 !== 0) {
        throw new Error("Invalid base64 input length")
    }

    const hasDoublePadding = sanitized.endsWith("==")
    const hasSinglePadding = !hasDoublePadding && sanitized.endsWith("=")
    const padding = hasDoublePadding ? 2 : hasSinglePadding ? 1 : 0
    const outputLength = (sanitized.length / 4) * 3 - padding
    const bytes = new Uint8Array(outputLength)

    let outputIndex = 0
    for (let index = 0; index < sanitized.length; index += 4) {
        const char0 = sanitized.charCodeAt(index)
        const char1 = sanitized.charCodeAt(index + 1)
        const char2 = sanitized.charCodeAt(index + 2)
        const char3 = sanitized.charCodeAt(index + 3)

        const sextet0 = BASE64_LOOKUP[char0]
        const sextet1 = BASE64_LOOKUP[char1]
        const sextet2 = char2 === 61 ? 0 : BASE64_LOOKUP[char2]
        const sextet3 = char3 === 61 ? 0 : BASE64_LOOKUP[char3]

        if (sextet0 < 0 || sextet1 < 0 || (char2 !== 61 && sextet2 < 0) || (char3 !== 61 && sextet3 < 0)) {
            throw new Error("Invalid base64 input data")
        }

        const chunk =
            (sextet0 << 18) | (sextet1 << 12) | (sextet2 << 6) | sextet3

        if (outputIndex < outputLength) {
            bytes[outputIndex] = (chunk >> 16) & 255
            outputIndex += 1
        }
        if (outputIndex < outputLength) {
            bytes[outputIndex] = (chunk >> 8) & 255
            outputIndex += 1
        }
        if (outputIndex < outputLength) {
            bytes[outputIndex] = chunk & 255
            outputIndex += 1
        }
    }

    return bytes
}

function base64ToBytes(base64) {
    if (typeof globalThis.atob === "function") {
        return base64ToBytesWithAtob(base64)
    }
    return base64ToBytesWithoutAtob(base64)
}

export default function createDeepFilterWasmModuleSync() {
    if (!initialized) {
        wasmBindgen.initSync(base64ToBytes(DEEPFILTER_WASM_BASE64))
        initialized = true
    }

    return wasmBindgen
}
`
fs.writeFileSync(syncOutPath, syncModule, "utf8")

console.log("Generated DeepFilterNet worklet artifacts:")
console.log(`- ${bindgenOutPath}`)
console.log(`- ${wasmBase64OutPath}`)
console.log(`- ${syncOutPath}`)
